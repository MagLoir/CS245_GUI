    //method:randomize()
    //purpose:randomize stuff
    public void randomize()
    {
        Random r = new Random();
        display = words[r.nextInt(words.length)];
        displayWord.setText(display);
        
        switch(r.nextInt(words.length)){
            case 0:
                displayWord.setForeground(Color.RED);
                wordColor = "RED";
                break;
            case 1:
                displayWord.setForeground(Color.YELLOW);
                wordColor = "YELLOW";
                break;
            case 2:
                displayWord.setForeground(Color.GREEN);
                wordColor = "GREEN";
                break;
            case 3:
                displayWord.setForeground(Color.BLUE);
                wordColor = "BLUE";
                break;
            case 4:
               displayWord.setForeground(Color.MAGENTA);
               wordColor = "MAGENTA";
               break;
        }
        
        // set all panels to invisible && reset tracker
        counts.clear();
        for(int j=0; j<panels.length; ++j)
        {
            panels[j].setOpaque(false); 
        }

       
        // randomizes the button locations
        for(int i=0; i<buttons.length; ++i)
        {
            int randIndex = r.nextInt(panels.length-2);
            // ensure random panel pulled not duplicated
            while(isIn(randIndex))
            {
                randIndex = r.nextInt(panels.length-2);
            }
            counts.add(randIndex);
            
            // add button to randomly pulled jpanel location
            panels[randIndex].add(buttons[i]);
            panels[randIndex].setVisible(true);
            buttons[i].setVisible(true);
            
            System.out.println(i+ "=" +randIndex);
        }

    }
    
    //method: isIn(int n)
    //purpose: checks an ArrayList for a number
    private Boolean isIn(int n)
    {
        // return false as soon as number detected
        for(int i=0; i<counts.size(); ++i)
        {
            if(n == counts.get(i))
            {
                return true;
            }
        }
        return false;
    }
    
