    // returns index if score qualifies, else returns -1 if unqualified
    private int isHighScore()
    {
        // if score is less than or equal to lowest score
        if( yourScore <= Integer.parseInt(highScores.get(4).get(1)) )
        {
            return -1;
        }
        
        // if score greater than highest score
        else if( yourScore > Integer.parseInt(highScores.get(0).get(1)) )
        {
            return 0;
        }
        // if score is somewhere in between, return index where to be inserted
        // (starting with lowest high score)
        else{
            for(int i=highScores.size()-1; i>0; --i)
            {
                if( yourScore > Integer.parseInt(highScores.get(i).get(1)) 
                        &&  yourScore <= Integer.parseInt(highScores.get(i-1).get(1)) )
                {
                    return i;
                }
            }
        }
        
        // if something went wrong
        return -2;
    }
    
    
    // reads from a file and returns a 2D array
    private ArrayList<ArrayList<String>> loadHighScores()
    {
        ArrayList<ArrayList<String>> highScores = new ArrayList<>();
        try{
            BufferedReader in = new BufferedReader(new FileReader("Top5.txt"));
            String s = in.readLine();
            int i=0;
            while(s != null)
            {
                // parse string by space and add to 2D Array
                String[] str = s.split("\\s+"); 
                highScores.add(new ArrayList<String>());
                highScores.get(i).add(str[0]);
                highScores.get(i).add(str[1]);
                
                // read next line
                s = in.readLine();
                ++i;
            }
            in.close();
        }catch (IOException ex) 
        {
            Logger.getLogger(HighScores.class.getName()).log(Level.SEVERE, null, ex);
        } 
            
        return highScores;
    }
    
    // updates array containing high scores
    private void updateHighScores()
    {
        try{
            BufferedWriter out = new BufferedWriter(new FileWriter("Top5.txt"));
            
            // just copy over unchanged high scores (starting with first place)
            for(int i=0; i<index; ++i)
            {
                out.write(highScores.get(i).get(0)+ " "+ highScores.get(i).get(1));
                out.newLine(); // continue writing to file in newLine
            }
            
            // copy over new high score
            out.write(winningName+ " "+ String.valueOf(yourScore));
            out.newLine();
            
            // demote remaining high scores 
            for(int i=index; i<highScores.size()-1; ++i)
            {
                out.write(highScores.get(i).get(0)+ " "+ highScores.get(i).get(1));
                out.newLine();
            }            
            // close BufferWriter
            out.close();
        }catch (IOException ex) 
        {
            Logger.getLogger(HighScores.class.getName()).log(Level.SEVERE, null, ex);
        }    
    }
    
    
